const addDefaultResourceLinks = (subjects: any[]) => {
    return subjects.map(subject => {
        const hasSpecificLink = subject.resourceUrl && subject.resourceUrl !== '#' && subject.resourceUrl.trim() !== '';
        const linkFromMap = subjectResourceLinks[subject.code];

        if (hasSpecificLink) {
            return subject; // Keep the existing specific link
        }
        
        if (linkFromMap) {
            return { ...subject, resourceUrl: linkFromMap }; // Use the link from the map
        }
        
        return { ...subject, resourceUrl: DEFAULT_RESOURCE_LINK }; // Fallback to the default link
    });
};
  
